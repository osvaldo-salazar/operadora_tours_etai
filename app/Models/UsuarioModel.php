class UsuarioModel {

    public static function login($usuario) {

        global $conexion;

        $sql = "SELECT * FROM usuarios 
                WHERE usuario_usuario = :usuario 
                AND estado_usuario = 1";

        $stmt = $conexion->prepare($sql);
        $stmt->bindParam(":usuario", $usuario);
        $stmt->execute();

        return $stmt->fetch(PDO::FETCH_ASSOC);
    }

}
